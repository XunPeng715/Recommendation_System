package step4;


public class Co_concurrence {

	private int itemsID1;
	private int itemsID2;
	private int num;
	
	public Co_concurrence(int itemsID1, int itemsID2, int num){
		this.itemsID1 = itemsID1;
		this.itemsID2 = itemsID2;
		this.num = num;
	}

	public int getItemsID1() {
		return itemsID1;
	}

	public void setItemsID1(int itemsID1) {
		this.itemsID1 = itemsID1;
	}

	public int getItemsID2() {
		return itemsID2;
	}

	public void setItemsID2(int itemsID2) {
		this.itemsID2 = itemsID2;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
	
	
}
